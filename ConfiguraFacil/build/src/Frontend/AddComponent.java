    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        String name = this.name.getText();
        String price = this.price.getText();
        int type = this.typeBox.getSelectedIndex();
        String desc = this.description.getText();
        int quantitySlider = this.quantityslider.getValue();
        String compability = this.compabilityBox1.getSelectedItem().toString();
        Boolean flag = false;
        Error error = new Error();
        
        if(!name.isEmpty() && !price.isEmpty() && !desc.isEmpty() && quantitySlider != 0 && !compability.isEmpty() && type !=0){
            this.cf =  new ConfiguraFacil();
            flag = false;
            try {
                 double pr = Double.valueOf(price);
                 Component component = new Component(name,pr,quantitySlider,desc,type,compability); //!!
                 flag = this.cf.addComponent(component);
            } catch (Exception ex) {
                System.out.println("Error: " +ex.getMessage());
                error.showError(ex.getMessage());
            }
                
            if(flag){
                Success success = new Success();
                success.showSuccess(this.user,"Admin");
                this.hide();
                    
            } else {
                error.showError("Unable to add Component!!");
            }
        } else {
             error.showError("Please complete all required text fields!!");
        }
    }//GEN-LAST:event_confirmButtonActionPerformed

